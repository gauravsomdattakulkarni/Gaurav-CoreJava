public class ConditionalStatement {
    public static void main(String[] args) {
        int num = 12;
        if(num==0)
        {
            System.out.println("The Number Is Zero");
        }
        else if(num%2==0)
        {
            System.out.println("The Number Is Even");
        }
        else
        {
            System.out.println("The Number Is Odd");
        }
    }
}
